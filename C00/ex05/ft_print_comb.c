#include <unistd.h>

void	ft_print_comb(void)
{
	int	n1;
	int	n2;
	int	n3;

	n1 = 0;
	while (n1 <= 7)
	{
		n2 = n1 + 1;
		while (n2 <= 8)
		{
			n3 = n2 + 1;
			while (n3 <= 9)
			{
				write(1, &"0123456789"[n1], 1);
				write(1, &"0123456789"[n2], 1);
				write(1, &"0123456789"[n3], 1);
				if (!(n1 == 7 && n2 == 8 && n3 == 9))
					write(1, ", ", 2);
				n3++;
			}
			n2++;
		}
		n1++;
	}
}

